// Estrutura de repetição DO While.
/*O bloco de codigo é executado pelo menos uma vez e em seguida é verificado a condição.*/

var contador = 10;

while (contador < 10)
{
    Console.WriteLine(contador);
    contador++;
}
do
{
    Console.WriteLine(contador);
    contador++;
}
while (contador < 10);

